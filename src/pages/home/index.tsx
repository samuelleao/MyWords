import Head from "next/head";

import { Container, Input, Button, Text } from "@styles/index";

import { HomeMain, HomeHeader, HomeFormSearch } from "./styles";

import { ResultWord } from "@components/ResultWord";
import { WordsSection } from "./components/WordsSection";
import { useEffect, useRef, useState } from "react";
import { getAPI } from "utils/getApi";
import { Toast } from "@components/Toast";
import { FavoritesArea } from "./components/FavoritesArea";

interface dataAPI {
  word: string;
  text: string;
  audio: string;
  type: string;
  phraseExample: string;
  id: any;
}

export default function Home() {
  const [word, setWord] = useState<dataAPI>({
    word: "",
    text: "",
    audio: "",
    type: "",
    phraseExample: "",
    id: "",
  });

  const [error, setError] = useState(false);

  const inputSearchRef = useRef<HTMLInputElement>(null);

  const handleAPI = async (wordWriter: string, id: any = null) => {
    if (id == null) {
      id = Date.now() + Math.random();
    }

    try {
      const response = await getAPI<any>(
        `https://api.dictionaryapi.dev/api/v2/entries/en/${wordWriter}`
      );

      const responseJson = response.data[0];
      const { word, phonetics, meanings } = responseJson;
      const { partOfSpeech, definitions } = meanings[0];
      const { definition } = definitions[0];
      const [text, audio] = phonetics;

      setWord({
        word: word,
        text: text?.text,
        audio: audio?.audio,
        type: partOfSpeech,
        phraseExample: definition,
        id: id,
      });

      window.scrollTo(0, 0);
      setError(false);
    } catch (error) {
      setError(true);
      throw (error as Error).message;
    }

    return null;
  };

  useEffect(() => {
    handleAPI("word", 1668384142161.6328);
  }, []);

  return (
    <>
      <Head>
        <title>My Words</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <HomeMain>
        <Container>
          <Toast
            control={error}
            setControl={setError}
            title="Ops! Word not found"
            description="Can't find that word in our database"
          />

          <HomeHeader>
            <Text size="lg" weight="semibold">
              MyWords
            </Text>
            <FavoritesArea />
          </HomeHeader>
          <HomeFormSearch>
            <Input
              placeholder="Type the word here..."
              css={{ width: "85%" }}
              ref={inputSearchRef}
            />
            <Button
              size="lg"
              type="brand"
              css={{ width: "15%" }}
              onClick={() => handleAPI(inputSearchRef?.current.value)}
            >
              Search
            </Button>
          </HomeFormSearch>
          <ResultWord
            world={word?.word}
            text={word?.text}
            audio={word?.audio}
            type={word?.type}
            id={word.id}
            phraseExample={word?.phraseExample}
          />
          <WordsSection handleAPIProps={handleAPI} />
        </Container>
      </HomeMain>
    </>
  );
}
